function show1m() {
	var a = document.getElementById("p11m");
	var b = document.getElementById("p1m");
	var c = document.getElementById("hide1m");
	var d = document.getElementById("show1m");
	var e = document.getElementById("table1m");
	if (getComputedStyle(a, b, c, d, e, null).display === "none") {
		a.style.display = "block";
		b.style.display = "none"
		c.style.display = "block";
		d.style.display = "none";
		e.style.width = "300px";
		e.style.transition = "width 0.15s"
	} 
	else {
		a.style.display = "none";
    }
}

