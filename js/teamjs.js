function show1m() {
	var a = document.getElementById("span21m");
	var b = document.getElementById("hide1m");
	var c = document.getElementById("show1m");
	var d = document.getElementById("table1m");
	if (getComputedStyle(a, b, c, d, null).display === "none") {
		a.style.display = "block";
		b.style.display = "block";
		c.style.display = "none";
		d.style.width = "300px";
		d.style.transition = "width 0.15s"
	} 
	else {
		a.style.display = "none";
    }
}

